/* Returns raw 12-bit ADC value (0..4095) for channel index 0..3 */
uint16_t ADC_GetRaw(uint8_t ch)
{
  if (ch >= ADC_CHANNELS) return 0;
  return adc_buf[ch];
}

/* Returns voltage in millivolts (0..3300 mV) for channel index 0..3 */
uint32_t ADC_GetMillivolts(uint8_t ch)
{
  return ((uint32_t)ADC_GetRaw(ch) * ADC_VREF_MV) / ADC_MAX_VAL;
}

/* Returns voltage as float in volts (0.0..3.3 V) for channel index 0..3 */
float ADC_GetVolts(uint8_t ch)
{
  return (float)ADC_GetRaw(ch) * (3.3f / 4095.0f);
}

